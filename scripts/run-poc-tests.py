def resolve_paths(cwd: Path) -> dict[str, Path]:
    state = cwd / ".agentic-security"
    return {
        "scan":       state / "last-scan.json",
        "poc":        state / "poc",
        "cache":      state / "poc-cache",
        "history":    state / "validator-history.json",
        "auth":       state / "auth-fixtures.json",
        "post_tmpl":  SCRIPT_DIR / "validator" / "post_exploit_templates.json",
        "chain_rules": SCRIPT_DIR / "validator" / "chain_rules.json",
    }


def load_scan(scan_path: Path) -> list[dict]:
    if not scan_path.exists():
        print(f"ERROR: {scan_path} not found — run /scan --all first.", file=sys.stderr)
        sys.exit(1)
    data = json.loads(scan_path.read_text())
    findings = []
    for key in ("findings", "logicVulns", "supplyChain"):
        findings.extend(data.get(key, []))
    return findings


def sev_idx(sev: str) -> int:
    try:
        return SEVERITY_ORDER.index((sev or "info").lower())
    except ValueError:
        return len(SEVERITY_ORDER)


# ─────────────────────────────────────────────────────────────────────────────
# 4. Framework detection (reuses validator/detect-framework.mjs when present)
# ─────────────────────────────────────────────────────────────────────────────

def detect_framework(cwd: Path) -> dict:
    script = SCRIPT_DIR / "validator" / "detect-framework.mjs"
    if script.exists():
        result = subprocess.run(
            ["node", str(script), str(cwd)],
            capture_output=True, text=True, timeout=10, cwd=str(cwd),
        )
        if result.returncode == 0:
            try:
                return json.loads(result.stdout)
            except json.JSONDecodeError:
                pass

    # Fallback sniff
    pkg = cwd / "package.json"
    if pkg.exists():
        try:
            pkg_data = json.loads(pkg.read_text())
            deps = {**pkg_data.get("dependencies", {}), **pkg_data.get("devDependencies", {})}
            if "vitest" in deps:
                return {"framework": "vitest", "runner": "npx vitest run", "ext": "test.ts", "lang": "ts"}
            if "jest" in deps:
                return {"framework": "jest", "runner": "npx jest", "ext": "test.js", "lang": "js"}
            if "mocha" in deps:
                return {"framework": "mocha", "runner": "npx mocha", "ext": "test.js", "lang": "js"}
        except Exception:
            pass
        return {"framework": "node-test", "runner": "node --test", "ext": "test.js", "lang": "js"}
    if any((cwd / p).exists() for p in ("pyproject.toml", "setup.py", "pytest.ini")):
        return {"framework": "pytest", "runner": "pytest -v", "ext": "test.py", "lang": "python"}
    if (cwd / "go.mod").exists():
        return {"framework": "go-test", "runner": "go test", "ext": "test.go", "lang": "go"}
    if (cwd / "Cargo.toml").exists():
        return {"framework": "cargo-test", "runner": "cargo test", "ext": "rs", "lang": "rust"}
    return {"framework": "none", "runner": None, "ext": None, "lang": None}


def build_command(framework: str, test_file: Path, cwd: Path) -> list[str] | None:
    if framework == "go-test":
        return ["go", "test", "-run", ".", str(test_file.parent)]
    if framework == "cargo-test":
        manifest = test_file.parent
        while manifest != cwd and manifest != manifest.parent and not (manifest / "Cargo.toml").exists():
            manifest = manifest.parent
        if not (manifest / "Cargo.toml").exists():
            manifest = cwd
        return ["cargo", "test", "--manifest-path", str(manifest / "Cargo.toml")]
    if framework == "dotnet-test":
        return ["dotnet", "test", "--filter", f"FullyQualifiedName~{test_file.stem}"]
    if framework == "junit":
        return ["mvn", "test", f"-Dtest={test_file.stem}"]
    base = FRAMEWORK_RUNNERS.get(framework)
    if not base:
        return None
    return [*base, str(test_file)]


# ─────────────────────────────────────────────────────────────────────────────
# 5. Test execution primitive
# ─────────────────────────────────────────────────────────────────────────────

def run_test(test_file: Path, framework: str, timeout: int, cwd: Path,
             extra_env: dict[str, str] | None = None) -> dict:
    cmd = build_command(framework, test_file, cwd)
    if not cmd:
        return {"verdict": "INDETERMINATE_TEST_INVALID",
                "reason": f"no runner for framework: {framework}",
                "exit_code": -1, "duration_ms": 0, "stdout": "", "stderr": "", "command": ""}

    env = {**os.environ, **(extra_env or {})}
    t0 = time.monotonic()
    try:
        proc = subprocess.run(
            cmd, capture_output=True, text=True, timeout=timeout, cwd=str(cwd), env=env,
        )
        duration_ms = int((time.monotonic() - t0) * 1000)
        verdict = "TP_PROVEN" if proc.returncode != 0 else "PROBABLE_FP_VERIFIED"
        return {
            "verdict": verdict, "exit_code": proc.returncode, "duration_ms": duration_ms,
            "stdout": proc.stdout[:6000], "stderr": proc.stderr[:3000], "command": " ".join(cmd),
        }
    except subprocess.TimeoutExpired:
        return {"verdict": "INDETERMINATE_TEST_INVALID", "reason": f"timed out after {timeout}s",
                "exit_code": -1, "duration_ms": int((time.monotonic() - t0) * 1000),
                "stdout": "", "stderr": "", "command": " ".join(cmd)}
    except FileNotFoundError as e:
        return {"verdict": "INDETERMINATE_TEST_INVALID", "reason": f"runner not found: {e}",
                "exit_code": -1, "duration_ms": 0, "stdout": "", "stderr": "", "command": " ".join(cmd)}


