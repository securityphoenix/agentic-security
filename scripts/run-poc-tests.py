def find_chains(primary: dict, all_findings: list[dict], rules: list[dict]) -> list[dict]:
    """For a TP_PROVEN finding, find other findings in the scan that combine
       with it under a known chain rule. Returns enriched chain descriptions.
    """
    out = []
    p_cwe = cwe_of(primary)
    if not p_cwe:
        p_cwe = vuln_family(primary)
    for rule in rules:
        if not cwe_matches(p_cwe, rule.get("primary_cwe", "")):
            continue
        for other in all_findings:
            if other.get("id") == primary.get("id"):
                continue
            o_cwe = cwe_of(other) or vuln_family(other)
            if not cwe_matches(o_cwe, rule.get("secondary_cwe", "")):
                continue
            if rule.get("same_file_required") and other.get("file") != primary.get("file"):
                continue
            narrative = rule.get("narrative", "")
            narrative = (narrative
                         .replace("{primary.file}", primary.get("file", "?"))
                         .replace("{primary.line}", str(primary.get("line", "?")))
                         .replace("{secondary.file}", other.get("file", "?"))
                         .replace("{secondary.line}", str(other.get("line", "?"))))
            out.append({
                "name": rule.get("name", "chain"),
                "secondary_id": other.get("id"),
                "chained_severity": rule.get("chained_severity", "high"),
                "narrative": narrative,
            })
    return out


# ─────────────────────────────────────────────────────────────────────────────
# 14. NEW: Fix verification
# ─────────────────────────────────────────────────────────────────────────────

def verify_fix(result: dict) -> str:
    """Invert the verdict semantics: when verifying a fix, we WANT the test
       to pass (assertion no longer fires). Returns FIX_VERIFIED or
       FIX_INCOMPLETE; INDETERMINATE passes through.
    """
    v = result["verdict"]
    if v == "INDETERMINATE_TEST_INVALID":
        return v
    if v == "PROBABLE_FP_VERIFIED":  # test passed = vuln blocked = fix worked
        return "FIX_VERIFIED"
    if v == "TP_PROVEN":             # test still fails = vuln still triggers
        return "FIX_INCOMPLETE"
    return v


