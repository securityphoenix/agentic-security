# 9. NEW: Adversarial variants
# ─────────────────────────────────────────────────────────────────────────────

def run_variants(poc_file: Path, framework: str, timeout: int, cwd: Path,
                 extra_env: dict[str, str] | None = None) -> list[dict]:
    """Run each variant in variants.json by injecting AS_POC_PAYLOAD env var.

    Test files generated by the agent should honor AS_POC_PAYLOAD when set:
      const payload = process.env.AS_POC_PAYLOAD || "<canonical>";

    Each variant becomes a separate run; we report which fire (TP) and which
    are blocked (PASSED). The set of firing variants tells you which
    encodings/bypasses get past the application's filters.
    """
    variants = load_variants(poc_file)
    if not variants:
        return []
    base_env = dict(extra_env or {})
    out = []
    for v in variants:
        label = v.get("label", "unnamed")
        payload = v["payload"]
        env = {**base_env, "AS_POC_PAYLOAD": payload, "AS_POC_VARIANT": label}
        r = run_test(poc_file, framework, timeout, cwd, extra_env=env)
        out.append({
            "label": label,
            "payload": payload,
            "verdict": r["verdict"],
            "duration_ms": r["duration_ms"],
        })
    return out


