def colored(text: str, verdict: str, use_color: bool) -> str:
    if not use_color:
        return text
    return f"{VERDICT_COLOR.get(verdict, '')}{text}{RESET}"


def print_result_line(r: PoCResult, use_color: bool) -> None:
    icon = VERDICT_ICONS.get(r.verdict, r.verdict)
    sev = (r.finding.get("severity") or "?").lower()
    fid = r.finding.get("id", "?")
    label = colored(f"[{icon}]", r.verdict, use_color)
    suffix = ""
    if r.verdict == "CACHED":
        suffix = f"  (cached: {r.reason})"
    elif r.verdict == "NO_POC":
        suffix = "  (no PoC — run /validate-findings first)"
    elif r.duration_ms:
        suffix = f"  ({r.duration_ms}ms)"
    print(f"{label} {sev:<8} {fid[:55]:<55}{suffix}")


def print_variant_matrix(r: PoCResult, use_color: bool) -> None:
    if not r.variants:
        return
    print(f"       variants:")
    for v in r.variants:
        mark = "FIRES " if v["verdict"] == "TP_PROVEN" else \
               "blocked" if v["verdict"] == "PROBABLE_FP_VERIFIED" else "error"
        c = "TP_PROVEN" if v["verdict"] == "TP_PROVEN" else "PROBABLE_FP_VERIFIED"
        print(f"         {colored(mark, c, use_color):<22} {v['label']:<22} ({v['duration_ms']}ms)")


def print_auth_matrix(r: PoCResult, use_color: bool) -> None:
    if not r.auth_matrix:
        return
    print(f"       auth-ladder:")
    for level, verdict in r.auth_matrix.items():
        c = "TP_PROVEN" if verdict == "TP_PROVEN" else "PROBABLE_FP_VERIFIED"
        mark = "FIRES " if verdict == "TP_PROVEN" else "blocked"
        print(f"         {colored(mark, c, use_color):<14} {level}")


def print_chains(r: PoCResult, use_color: bool) -> None:
    if not r.chains:
        return
    print(f"       attack chains:")
    for ch in r.chains:
        label = colored(f"  ⛓ {ch['name']} → severity={ch['chained_severity']}", "TP_PROVEN", use_color)
        print(f"       {label}")
        for line in ch["narrative"].split(". "):
            line = line.strip()
            if line:
                print(f"         {line}{'.' if not line.endswith('.') else ''}")


def print_post_exploit(r: PoCResult, use_color: bool) -> None:
    pe = r.post_exploit
    if not pe:
        return
    print(f"       post-exploit ({pe.get('name', '?')}):")
    blast = pe.get("blast_radius", "")
    if blast:
        print(f"         blast radius: {blast}")
    for step in pe.get("next_steps", []):
        print(f"           ↳ {step}")


def emit_junit(results: list[PoCResult], path: Path) -> None:
    suite = ET.Element("testsuite", name="agentic-security-poc", tests=str(len(results)))
    for r in results:
        fid = r.finding.get("id", "?")
        tc = ET.SubElement(suite, "testcase", name=fid, classname=r.finding.get("vuln", "unknown"))
        tc.set("time", str(r.duration_ms / 1000))
        v = r.verdict
        if v in ("TP_PROVEN", "TP_PROVEN_OOB", "FIX_INCOMPLETE"):
            fail = ET.SubElement(tc, "failure", message=f"{v}: vulnerability confirmed")
            fail.text = (r.stdout or "")[:2000]
        elif v == "INDETERMINATE_TEST_INVALID":
            err = ET.SubElement(tc, "error", message=r.reason or "test errored")
            err.text = (r.stderr or "")[:2000]
        elif v == "NO_POC":
            ET.SubElement(tc, "skipped", message="No PoC test generated yet — run /validate-findings first")
    tree = ET.ElementTree(suite)
    ET.indent(tree, space="  ")
    tree.write(str(path), encoding="unicode", xml_declaration=True)
    print(f"\nJUnit XML → {path}")


def emit_evidence_bundle(results: list[PoCResult], cwd: Path, out_path: Path) -> None:
    """Pack TP_PROVEN findings + their PoCs + outputs into a single JSON
       evidence bundle suitable for a pentest report or client deliverable.
    """
    bundle = {
        "generated_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "project": str(cwd),
        "findings": [],
    }
    for r in results:
        if r.verdict not in ("TP_PROVEN", "TP_PROVEN_OOB", "FIX_INCOMPLETE"):
            continue
        entry = {
            "id": r.finding.get("id"),
            "vuln": r.finding.get("vuln"),
            "cwe": r.finding.get("cwe"),
            "severity": r.finding.get("severity"),
            "file": r.finding.get("file"),
            "line": r.finding.get("line"),
            "verdict": r.verdict,
            "command": r.command,
            "duration_ms": r.duration_ms,
            "stdout_tail": (r.stdout or "")[-2000:],
            "stderr_tail": (r.stderr or "")[-2000:],
            "poc_file": r.poc_file,
            "variants": r.variants,
            "auth_matrix": r.auth_matrix,
            "oob_hit": r.oob_hit,
            "chains": r.chains,
            "post_exploit": r.post_exploit,
        }
        # Inline the test file content too, so the bundle is self-contained
        try:
            if r.poc_file and Path(r.poc_file).exists():
                entry["poc_test_source"] = Path(r.poc_file).read_text()[:20000]
        except Exception:
            pass
        bundle["findings"].append(entry)
    out_path.write_text(json.dumps(bundle, indent=2))
    print(f"\nEvidence bundle → {out_path}  ({len(bundle['findings'])} proven finding(s))")


# ─────────────────────────────────────────────────────────────────────────────
# 16. Main
# ─────────────────────────────────────────────────────────────────────────────

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Batch-execute PoC tests generated by /agentic-security:validate-findings",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    # Filters
    parser.add_argument("--severity", choices=SEVERITY_ORDER, default=None,
                        help="minimum severity to include (default: all)")
    parser.add_argument("--id", dest="finding_id", default=None,
                        help="run a single finding by ID")
    # Modes
    parser.add_argument("--list", action="store_true",
                        help="list available PoCs without running")
    parser.add_argument("--variants", action="store_true",
                        help="run all adversarial variants per finding (WAF/filter bypass surface)")
    parser.add_argument("--auth-ladder", action="store_true",
                        help="run each PoC under unauth/low-priv/high-priv contexts")
    parser.add_argument("--verify-fix", action="store_true",
                        help="confirm previously-proven findings no longer fire after /fix")
    parser.add_argument("--chains", action="store_true",
                        help="correlate TP_PROVEN findings into multi-step attack chains")
    parser.add_argument("--blind", action="store_true",
                        help="enable OOB callback listener for blind SQLi/SSRF/XSS")
    parser.add_argument("--post-exploit", action="store_true",
                        help="show post-exploitation suggestions for each TP_PROVEN finding")
    # Output
    parser.add_argument("--json", action="store_true", help="output results as JSON")
    parser.add_argument("--junit", default=None, help="write JUnit XML to this path")
    parser.add_argument("--evidence", default=None,
                        help="write self-contained evidence bundle (JSON) to this path")
    # Runtime
    parser.add_argument("--timeout", type=int, default=60,
                        help="per-test timeout in seconds (default: 60)")
    parser.add_argument("--no-cache", action="store_true", help="ignore cached verdicts")
    parser.add_argument("--cwd", default=None, help="project root (default: current directory)")
    args = parser.parse_args()

    cwd = Path(args.cwd).resolve() if args.cwd else Path.cwd()
    use_color = sys.stdout.isatty() and not args.json
    paths = resolve_paths(cwd)
    findings = load_scan(paths["scan"])
    fw = detect_framework(cwd)
    auth_fixture = load_auth_fixture(paths["auth"])
    post_templates = load_post_exploit_templates(paths["post_tmpl"])
    chain_rules = load_chain_rules(paths["chain_rules"])

    # Filter
    min_sev = sev_idx(args.severity) if args.severity else len(SEVERITY_ORDER)
    targets = []
    for f in findings:
        if args.finding_id and f.get("id") != args.finding_id:
            continue
        if sev_idx(f.get("severity", "info")) > min_sev:
            continue
        targets.append(f)
    if not targets:
        print("No findings match the given filters.")
        sys.exit(0)

    if args.list:
        print(f"{'ID':<60}  {'SEV':<8}  {'POC?':<5}  {'VARIANTS':<8}")
        print("-" * 90)
        for f in targets:
            poc = find_poc_file(paths["poc"], f["id"])
            has = "yes" if poc else "no"
            vcount = len(load_variants(poc)) if poc else 0
            print(f"{f['id'][:60]:<60}  {f.get('severity','?'):<8}  {has:<5}  {vcount:<8}")
        sys.exit(0)

    # Banner
    print(f"Framework:      {fw.get('framework','none')}  ({fw.get('runner') or 'n/a'})")
    if args.auth_ladder:
        print(f"Auth fixture:   {paths['auth']}  ({'loaded' if auth_fixture else 'MISSING — using defaults'})")
    oob: OOBListener | None = None
    if args.blind:
        oob = OOBListener()
        oob.start()
        print(f"OOB listener:   {oob.base_url}  (token-based blind-vuln detection)")
    if args.verify_fix:
        print("Mode:           --verify-fix (inverted verdicts: PASS = fix worked)")
    print(f"\nRunning PoCs for {len(targets)} finding(s)...\n")

    results: list[PoCResult] = []

    for f in targets:
        fid = f["id"]
        r = PoCResult(finding=f, verdict="SKIPPED")

        # Cache
        if not args.no_cache and not args.verify_fix:
            cached = read_cache(paths["cache"], f, cwd)
            if cached:
                r.verdict = "CACHED"
                r.reason = cached.get("verdict", "?")
                results.append(r)
                print_result_line(r, use_color)
                continue

        # Locate PoC
        poc_file = find_poc_file(paths["poc"], fid)
        if not poc_file:
            r.verdict = "NO_POC"
            results.append(r)
            print_result_line(r, use_color)
            continue
        r.poc_file = str(poc_file)

        # Inject OOB callback URL if blind mode active
        extra_env: dict[str, str] = {}
        oob_token = ""
        if oob:
            oob_token = uuid.uuid4().hex[:16]
            extra_env["AS_POC_OOB_URL"] = oob.token_url(oob_token)
            extra_env["AS_POC_OOB_TOKEN"] = oob_token

        # Run canonical
        run_result = run_test(poc_file, fw.get("framework", "none"), args.timeout, cwd,
                              extra_env=extra_env or None)
        r.verdict = run_result["verdict"]
        r.duration_ms = run_result["duration_ms"]
        r.exit_code = run_result.get("exit_code", -1)
        r.stdout = run_result.get("stdout", "")
        r.stderr = run_result.get("stderr", "")
        r.command = run_result.get("command", "")
        r.reason = run_result.get("reason", "")

        # OOB upgrade
        if oob and oob_token and oob.was_hit(oob_token):
            r.oob_hit = True
            if r.verdict in ("PROBABLE_FP_VERIFIED", "INDETERMINATE_TEST_INVALID"):
                # Test passed/errored, but the listener got a hit → blind vuln confirmed
                r.verdict = "TP_PROVEN_OOB"
            elif r.verdict == "TP_PROVEN":
                r.verdict = "TP_PROVEN_OOB"

        # --verify-fix: invert
        if args.verify_fix:
            r.verdict = verify_fix({"verdict": r.verdict})

        # --variants
        if args.variants and r.verdict in ("TP_PROVEN", "TP_PROVEN_OOB"):
            r.variants = run_variants(poc_file, fw.get("framework", "none"), args.timeout, cwd,
                                      extra_env=extra_env)

        # --auth-ladder
        if args.auth_ladder:
            fixture = auth_fixture or DEFAULT_AUTH_FIXTURE
            r.auth_matrix = run_auth_ladder(poc_file, fw.get("framework", "none"),
                                            args.timeout, cwd, fixture)

        # --chains
        if args.chains and r.verdict in ("TP_PROVEN", "TP_PROVEN_OOB", "FIX_INCOMPLETE"):
            r.chains = find_chains(f, findings, chain_rules)

        # --post-exploit
        if args.post_exploit and r.verdict in ("TP_PROVEN", "TP_PROVEN_OOB", "FIX_INCOMPLETE"):
            r.post_exploit = post_exploit_for(f, post_templates)

        # Cache canonical verdict (not in fix-verify mode, that's a separate axis)
        if not args.no_cache and not args.verify_fix and \
           r.verdict in ("TP_PROVEN", "PROBABLE_FP_VERIFIED", "TP_PROVEN_OOB"):
            write_cache(paths["cache"], f, cwd, {
                "verdict": r.verdict, "duration_ms": r.duration_ms, "oob_hit": r.oob_hit,
            })

        results.append(r)
        print_result_line(r, use_color)
        if args.variants:    print_variant_matrix(r, use_color)
        if args.auth_ladder: print_auth_matrix(r, use_color)
        if args.chains:      print_chains(r, use_color)
        if args.post_exploit:print_post_exploit(r, use_color)
        if r.verdict in ("TP_PROVEN", "TP_PROVEN_OOB", "FIX_INCOMPLETE", "INDETERMINATE_TEST_INVALID") and not args.json:
            tail = (r.stdout + r.stderr).strip().splitlines()[:12]
            if tail:
                print("       " + "\n       ".join(tail))
            print()

    if oob:
        oob.stop()

    # Summary
    counts: dict[str, int] = {}
    for r in results:
        counts[r.verdict] = counts.get(r.verdict, 0) + 1

    if not args.json:
        print("\n" + "─" * 60)
        print(f"Summary  ({len(results)} findings):")
        for v in sorted(counts):
            label = colored(f"  [{VERDICT_ICONS.get(v, v)}]", v, use_color)
            print(f"{label}  {v:<30} {counts[v]}")
        proven = counts.get("TP_PROVEN", 0) + counts.get("TP_PROVEN_OOB", 0)
        broke  = counts.get("FIX_INCOMPLETE", 0)
        if proven:
            print(f"\n  {proven} TP_PROVEN finding(s) — run /agentic-security:fix --all --high to remediate")
        if broke:
            print(f"\n  {broke} FIX_INCOMPLETE finding(s) — the fix did NOT block the original PoC")

    if args.json:
        print(json.dumps([asdict(r) for r in results], indent=2, default=str))

    if args.junit:
        emit_junit(results, Path(args.junit))
    if args.evidence:
        emit_evidence_bundle(results, cwd, Path(args.evidence))

    # Exit 1 if any proven (or incomplete fix) → CI gate
    bad = counts.get("TP_PROVEN", 0) + counts.get("TP_PROVEN_OOB", 0) + counts.get("FIX_INCOMPLETE", 0)
    sys.exit(1 if bad > 0 else 0)


if __name__ == "__main__":
    main()
