def find_poc_file(poc_dir: Path, finding_id: str) -> Path | None:
    if not poc_dir.exists():
        return None
    safe = finding_id.replace("/", "_").replace(":", "_")
    direct = poc_dir / safe
    if direct.exists():
        cands = sorted(direct.glob("poc.*"))
        if cands:
            return cands[0]
    for d in poc_dir.iterdir():
        if d.is_dir() and (finding_id in d.name or d.name in finding_id):
            cands = sorted(d.glob("poc.*"))
            if cands:
                return cands[0]
    return None


def load_variants(poc_file: Path) -> list[dict]:
    """Load .agentic-security/poc/<id>/variants.json. Schema:
       [{"label": "unicode-quote", "payload": "ʼ OR ʼ1ʼ=ʼ1"}, ...]
    """
    variants_path = poc_file.parent / "variants.json"
    if not variants_path.exists():
        return []
    try:
        data = json.loads(variants_path.read_text())
        if isinstance(data, list):
            return [v for v in data if isinstance(v, dict) and "payload" in v]
    except Exception:
        pass
    return []


# ─────────────────────────────────────────────────────────────────────────────
# 7. Cache
# ─────────────────────────────────────────────────────────────────────────────

def cache_key(finding: dict, cwd: Path) -> str:
    file_path = finding.get("file") or finding.get("sink", {}).get("file", "")
    abs_file = (cwd / file_path).resolve() if file_path else Path("/dev/null")
    try:
        file_hash = hashlib.sha256(abs_file.read_bytes()).hexdigest()[:12]
    except Exception:
        file_hash = "nofile"
    try:
        version = json.loads((cwd / "package.json").read_text()).get("version", "unknown")
    except Exception:
        version = "unknown"
    raw = f"{finding['id']}::{file_hash}::{version}"
    return hashlib.sha256(raw.encode()).hexdigest()[:24]


def read_cache(cache_dir: Path, finding: dict, cwd: Path) -> dict | None:
    f = cache_dir / f"{cache_key(finding, cwd)}.json"
    if f.exists():
        try:
            return json.loads(f.read_text())
        except Exception:
            return None
    return None


def write_cache(cache_dir: Path, finding: dict, cwd: Path, verdict: dict) -> None:
    cache_dir.mkdir(parents=True, exist_ok=True)
    (cache_dir / f"{cache_key(finding, cwd)}.json").write_text(json.dumps(verdict, indent=2))


# ─────────────────────────────────────────────────────────────────────────────
# 8. CWE / family classification
# ─────────────────────────────────────────────────────────────────────────────

def cwe_of(finding: dict) -> str:
    return (finding.get("cwe") or "").upper().strip()


def cwe_matches(actual: str, pattern: str) -> bool:
    if not pattern:
        return False
    return actual in {p.strip().upper() for p in pattern.split("|") if p.strip()}


def vuln_family(finding: dict) -> str:
    """Best-effort family classification for routing to post-exploit templates."""
    cwe = cwe_of(finding)
    if cwe:
        return cwe
    vuln = (finding.get("vuln") or "").lower()
    if "sql" in vuln and "inj" in vuln:        return "CWE-89"
    if "command inj" in vuln or "rce" in vuln: return "CWE-78"
    if "path trav" in vuln or "lfi" in vuln:   return "CWE-22"
    if "ssrf" in vuln:                          return "CWE-918"
    if "xss" in vuln:                           return "CWE-79"
    if "idor" in vuln:                          return "CWE-639"
    if "prototype pollut" in vuln:              return "CWE-1321"
    if "open redirect" in vuln:                 return "CWE-601"
    if "mass assign" in vuln:                   return "CWE-915"
    if "prompt inj" in vuln:                    return "LLM01"
    if "deserial" in vuln:                      return "CWE-502"
    if "xxe" in vuln:                           return "CWE-611"
    return "DEFAULT"


