# 11. NEW: Blind / OOB callback listener
# ─────────────────────────────────────────────────────────────────────────────

class OOBListener:
    """Tiny HTTP listener for out-of-band callback detection (blind SQLi/SSRF/XSS).

    Spins up a ThreadingHTTPServer on a random localhost port. Each test gets
    a unique token; the test injects http://127.0.0.1:<port>/<token> into its
    payload. After the test runs we check if the token was hit.
    """

    def __init__(self):
        self.hits: dict[str, list[dict]] = {}
        self._server: http.server.HTTPServer | None = None
        self._thread: threading.Thread | None = None
        self._port: int = 0
        self._lock = threading.Lock()

    def start(self) -> None:
        hits = self.hits
        lock = self._lock

        class Handler(http.server.BaseHTTPRequestHandler):
            def _record(self):
                token = self.path.lstrip("/").split("?")[0].split("/")[0] or "_root"
                with lock:
                    hits.setdefault(token, []).append({
                        "method": self.command, "path": self.path,
                        "client": self.client_address[0],
                        "ts": time.time(),
                    })
                self.send_response(200)
                self.end_headers()
                self.wfile.write(b"ok")

            def do_GET(self):    self._record()
            def do_POST(self):   self._record()
            def do_HEAD(self):   self._record()
            def log_message(self, *_a, **_kw): pass

        sock = socket.socket()
        sock.bind(("127.0.0.1", 0))
        self._port = sock.getsockname()[1]
        sock.close()

        self._server = http.server.ThreadingHTTPServer(("127.0.0.1", self._port), Handler)
        self._thread = threading.Thread(target=self._server.serve_forever, daemon=True)
        self._thread.start()

    @property
    def base_url(self) -> str:
        return f"http://127.0.0.1:{self._port}"

    def token_url(self, token: str) -> str:
        return f"{self.base_url}/{token}"

    def was_hit(self, token: str) -> bool:
        with self._lock:
            return token in self.hits and len(self.hits[token]) > 0

    def stop(self) -> None:
        if self._server:
            self._server.shutdown()
            self._server.server_close()


