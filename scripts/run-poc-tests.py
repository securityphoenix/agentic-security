DEFAULT_AUTH_FIXTURE = {
    "unauth": {"token": "", "user_id": ""},
    "low":    {"token": "AS_LOW_PRIV_TOKEN_REPLACE_ME", "user_id": "1"},
    "high":   {"token": "AS_HIGH_PRIV_TOKEN_REPLACE_ME", "user_id": "999"},
}


def load_auth_fixture(auth_path: Path) -> dict:
    if not auth_path.exists():
        return {}
    try:
        return json.loads(auth_path.read_text())
    except Exception:
        return {}


def run_auth_ladder(poc_file: Path, framework: str, timeout: int, cwd: Path,
                    fixture: dict) -> dict[str, str]:
    """Re-run the PoC at three auth levels. Test files should honor
       AS_POC_AUTH_LEVEL and AS_POC_AUTH_TOKEN env vars.
       Returns {level: verdict}.
    """
    if not fixture:
        return {}
    out: dict[str, str] = {}
    for level in ("unauth", "low", "high"):
        ctx = fixture.get(level, DEFAULT_AUTH_FIXTURE[level])
        env = {
            "AS_POC_AUTH_LEVEL": level,
            "AS_POC_AUTH_TOKEN": ctx.get("token", ""),
            "AS_POC_AUTH_USER_ID": str(ctx.get("user_id", "")),
        }
        r = run_test(poc_file, framework, timeout, cwd, extra_env=env)
        out[level] = r["verdict"]
    return out


