#!/usr/bin/env python3
"""
run-poc-tests.py — batch-execute PoC test files generated by
                   /agentic-security:validate-findings

Beyond running canonical PoCs, this script offers six red-team-grade modes:

  --variants       Run all adversarial variants of each PoC (WAF/filter bypass surface)
  --auth-ladder    Run each PoC under unauth / low-priv / high-priv contexts
  --verify-fix     After /fix has been applied, confirm the PoC no longer fires
  --chains         Correlate TP_PROVEN findings into multi-step attack chains
  --blind          Spin up a local OOB callback listener for blind SQLi/SSRF/XSS
  --post-exploit   Show post-exploitation suggestions for each TP_PROVEN finding

Flags can be combined. Defaults match prior behaviour (canonical PoC only).
Stdlib only — no third-party dependencies.

Project state used:
  .agentic-security/last-scan.json            scanner output
  .agentic-security/poc/<id>/poc.<ext>        generated PoC test file
  .agentic-security/poc/<id>/variants.json    optional, for --variants
  .agentic-security/auth-fixtures.json        optional, for --auth-ladder
  .agentic-security/poc-cache/                verdict cache
"""

